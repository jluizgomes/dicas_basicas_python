# DICAS DE PYTHON - Operadores Matemáticos, Comparativos e Lógicos

# Operadores matemáticos / aritméticos

# Soma
2 + 4
# Saída: 6

# Subtração
4 - 2
# Saída: 2

# Multiplicação
4 * 2
# Saída: 8

# Divisão
4 / 2
# Saída: 2.0

# Exponenciação
4 ** 2
# Saída: 16

# Resto da divisão (modulo)
4 % 2
# Saída: 0

# Divisão inteira
10 // 9
# Saída: 1

# Operadores comparativos

# Maior que - Retorna True se o primeiro valor for maior que o segundo
2 > 1
# Saída: True

# Menor que - Retorna True se o primeiro valor for menor que o segundo
1 < 2
# Saída: True

# Igual a - Retorna True se os dois valores forem iguais
1 == 1
# Saída: True

# Maior ou igual a - Retorna True se o primeiro valor for maior ou igual ao segundo
2 >= 1
# Saída: True

# Menor ou igual a - Retorna True se o primeiro valor for menor ou igual ao segundo
2 <= 1
# Saída: False

# Diferente de - Retorna True se os dois valores forem diferentes
2 != 1
# Saída: True

# Operadores lógicos

# and - Retorna True se todos os valores forem True
x = 1
(x > 0) and (x < 100)
# Saída: True

# or - Retorna True se pelo menos um dos valores forem True
x = 1
(x > 10) or (x < 100)
# Saída: True

# not - Retorna True se o valor for False
x = 1
not x < 100
# Saída: False

# Operadores de atribuição
# Atribuição de valores é a passagem de valores para variáveis, ou seja, a variável recebe o valor da expressão

# Atribuição simples
a = 2
# A variável 'a' recebe o valor 2

# Atribuição simples com mais de um valor
a = 1
b, c = 2
# A variável 'a' recebe o valor 1 e a variável 'b' e 'c' recebem o valor 2

# Atribuição de múltiplos valores
a, b, c = 2, 3, 4
# A variável 'a' recebe o valor 2, a variável 'b' recebe o valor 3 e a variável 'c' recebe o valor 4