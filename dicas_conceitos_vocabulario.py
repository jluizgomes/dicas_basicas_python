# Dicas de Python - Conceitos e Vocabularios

# Terminal

# O Terminal é um programa que permite ao usuário executar comandos e interagir com o sistema operacional.
# Para executar um comando no terminal, basta digitar o nome do comando e pressionar a tecla Enter.

# Exemplo:
# python -c "print('Hello World')"
# O -c é um flag que indica que o comando está em Python.
# O comando acima imprime a string Hello World.

# python hello_world.py
# O comando acima executa o arquivo hello_world.py.

# Text Editor
# O editor de texto é um programa que permite ao usuário escrever e editar codigos.

# IDE (Integrated Development Environment)
# O IDE é um programa que permite ao usuário escrever e editar codigos, a principal diferença de um Text Editor para uma IDE é que IDE é possui muito mais recursos quem o editor de texto.

# Style Guides
# O Style Guide é um documento que define o estilo ou padrão de um determinado projeto.

# Especificações de Projeto
# O Especificação de Projeto é um documento que define as especificações, lista de requerimentos que um projeto irá precisar. 

# Sintaxe
# A sintaxe é o conjunto de regras sobre como programar em determinada linguagem, sintaxe é a gramática para essa linguagem. As regras de sintaxe informam como armazenar dados, responder a condições específicas e controlar o fluxo de execução na língua.

# Debugging (Depuração)
# Um bug é um problema na forma como um programa é executado.
# Depuração é o processo de identificar a causa de um bug e modificar o código para corrigir o problema.

# Refactoring (Refatoração)
# Refatoração é o processo de reescrever partes de um codigo para torná-lo mais simples, mais eficiente, e mais fácil de entender e executá-lo.

# Standard Library (Biblioteca Padrão)
# Uma biblioteca padrão é o conjunto de ferramentas incluídas no core da linguagem com a qual você está trabalhando.
# A biblioteca inclui estruturas de dados centrais e ferramentas para trabalhar com dados em seu programa.

# Third-Party Libraries (Bibliotecas de Terceiros)
# Uma biblioteca de terceiros adiciona ferramentas e funcionalidades que não são cobertos pela linguagem que você está utilizando.

# Frameworks (Frameworks)
# Um framework é um pacote maior que ajuda a resolver um determinado problema. Alguns frameworks ajudam você cria jogos, visualizações de dados e web formulários.

# Error Handling (Tratamento de Erros)
# O tratamento de erros refere-se à escrita de código que antecipa erros que podem ocorrer e, em seguida, responde a esses erros.

# Testing (Testes)
# Testes são ações que você executa para verificar se um programa está funcionando corretamente.

# User Interface (Interface do Usuário)
# A interface do usuário é a parte da aplicação que o usuário interage.

# API (Interface de Programação)
# Uma interface de programação de aplicativos (API) é um especificação de como um programa pede a outro programa para informações. 

# GUI (Interface Gráfica)
# Uma interface gráfica do usuário (GUI) é uma interface fácil de usar que permite aos usuários clique em elementos na tela.

# CLI (Interface de Linha de Comando)
# Uma interface de linha de comando (CLI) permite que os usuários interajam com um programa digitando comandos em um terminal.

# Modules (Módulos)
# Um módulo é um arquivo Python que contém um conjunto de funções e variáveis.