# Dicas de Python - Tuplas

# Tuplas
# Tuplas são coleções imutáveis de dados. Os colchetes indicam uma tupla.
# Tuplas são imutáveis, ou seja, não podem ser alteradas.
tuplas = () # Criando tupla vazia
tuplas = (1, 2, 3, 4, 5) # Criando tupla com 5 elementos

# Podemos acessar os elementos da tupla como se fosse uma lista
tuplas = (1, 2, 3, 4, 5)
tuplas[0]
# Saida: 1

tuplas = (1, 2, 3, 4, 5)
tuplas[-1]
# Saida: 5

tuplas = (1, 2, 3, 4, 5)
tuplas[4]
# Saida: 5
