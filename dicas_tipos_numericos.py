# Dicas de Python - Tipos de dados numéricos

# Tipos de dados numéricos são int, float, complex e decimal

# Tipo int()
# Converte um número para inteiro e string para inteiro
int(3.3)
# Saida: 3

int('3')
# Saida: 3

# Tipo float()
# Converte um número para float e string para float
float(3)
# Saida: 3.0

float('3')
# Saida: 3.0

# Tipo round()
# Arredonda um número para o inteiro mais próximo
round(3.1415926, 2)
# Saida: 3.14

